    private void ManRelCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ManRelCliActionPerformed
        // Gerando relatorios;
        int confirma = JOptionPane.showConfirmDialog(null, "Comfirma a Impressao Deste Relatorio?", "Aten√ß√£o", JOptionPane.YES_NO_OPTION);
        
        if(confirma == JOptionPane.YES_OPTION){
            // imprimindo frameork com jaspReport
            try {
                // imprimindo impressao de um relatorio com a classe jasperprint
                JasperPrint print = JasperFillManager.fillReport("C:/relatorios/clientes.jasper",null,conexao);
                // a linha abaixo exibe o relatorio atraves da classe jasper viewer
                JasperViewer.viewReport(print ,false);
                
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_ManRelCliActionPerformed

